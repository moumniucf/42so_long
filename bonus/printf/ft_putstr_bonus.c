#include "ft_printf_bonus.h"

int	ft_putstr(char *s)
{
	int	x;

	if (!s)
		s = "(null)";
	x = 0;
	while (*s)
	{
		x += ft_putchar(*s);
		s++;
	}
	return (x);
}
